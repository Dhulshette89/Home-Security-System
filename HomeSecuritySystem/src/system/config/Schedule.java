package system.config;


/**
 * The Class Schedule.
 */
public class Schedule
{
	
	/** The hour from. */
	int hourFrom;
	
	/** The minutefrom. */
	int minutefrom;
	
	/** The hour to. */
	int hourTo;
	
	/** The minute to. */
	int minuteTo;
	
	/**
	 * Gets the hour from.
	 *
	 * @return the hour from
	 */
	public int getHourFrom() {
		return hourFrom;
	}
	
	/**
	 * Sets the hour from.
	 *
	 * @param hourFrom the new hour from
	 */
	public void setHourFrom(int hourFrom) {
		this.hourFrom = hourFrom;
	}
	
	/**
	 * Gets the minute from.
	 *
	 * @return the minute from
	 */
	public int getMinuteFrom() {
		return minutefrom;
	}
	
	/**
	 * Sets the minute from.
	 *
	 * @param minutefrom the new minute from
	 */
	public void setMinuteFrom(int minutefrom) {
		this.minutefrom = minutefrom;
	}
	
	/**
	 * Gets the hour to.
	 *
	 * @return the hour to
	 */
	public int getHourTo() {
		return hourTo;
	}
	
	/**
	 * Sets the hour to.
	 *
	 * @param hourTo the new hour to
	 */
	public void setHourTo(int hourTo) {
		this.hourTo = hourTo;
	}
	
	/**
	 * Gets the minute to.
	 *
	 * @return the minute to
	 */
	public int getMinuteTo() {
		return minuteTo;
	}
	
	/**
	 * Sets the minute to.
	 *
	 * @param minuteTo the new minute to
	 */
	public void setMinuteTo(int minuteTo) {
		this.minuteTo = minuteTo;
	}
	
	
	
	
	
	
}